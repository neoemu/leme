import SwiftUI

enum KeyboardShortcuts {
    static let commandPalette = KeyEquivalent("p")       // Cmd+Shift+P
    static let search = KeyEquivalent("k")               // Cmd+K
    static let logs = KeyEquivalent("l")                  // Cmd+Shift+L
    static let terminal = KeyEquivalent("t")              // Cmd+Shift+T
    static let detailPanel = KeyEquivalent("d")           // Cmd+Shift+D
    static let closeBottomPanel = KeyEquivalent("j")      // Cmd+Shift+J
}
